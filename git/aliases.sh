# *Many* other helpers I use day in, day out come via oh-my-zsh + git plugin

# Short version is enough in 99% of the cases
alias gs="gss"

# Add all and commit
alias gac="ga . && gcmsg"

# Add all and commit via gitmoji
alias gagc="ga . && gitmoji -c"

# Faster git-open
alias go="git-open"

# Push and open
alias gpo="gp && go"

# Undo last commit
alias gundo="git undo"

# Lazygit
alias lzg="lazygit"
